package operators;

public class LogicalAndBitWiseOperators {

	public static void main(String[] args) {
		int a = 10,b=15,c=20;
		System.out.println("Logical Operators");
		System.out.println((a>b)&&(a>c));
		System.out.println((a>b)||(a>c));
		System.out.println("Bitwise Operators");
		System.out.println((a>b)&(a>c));
		System.out.println((a>b)|(a>c));

	}

}
