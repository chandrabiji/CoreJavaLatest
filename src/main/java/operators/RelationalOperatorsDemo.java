package operators;

public class RelationalOperatorsDemo {

	public static void main(String[] args) {
		int a = 10,b=5;
		System.out.println(" > "+(a>b));
		System.out.println(" < "+(a<b));
		System.out.println(" >= "+(a>=b));
		System.out.println(" <= "+(a<=b));
		System.out.println(" == "+(a==b));
		System.out.println(" != "+(a!=b));

	}

}
