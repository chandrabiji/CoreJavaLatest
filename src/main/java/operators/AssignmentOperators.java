package operators;

public class AssignmentOperators {

	public static void main(String[] args) {
	int a = 10;
	System.out.println("A = "+a);
	a +=5;//a = a+5;
	System.out.println("A = "+a);
	a -=4;//a = a -4=11
	System.out.println("A = "+a);
	a *=2;
	System.out.println("A = "+a);
	a /=2;
	System.out.println("A = "+a);
	System.out.println(" == "+(a==11));
	System.out.println(" != "+(a!=10));
	

	}

}
